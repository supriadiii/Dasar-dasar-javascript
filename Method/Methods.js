let greeting = "Hello, World!";
console.log(greeting);

console.log(greeting.toUpperCase());
// Mengubah semua karakter dalam string greeting menjadi huruf besar.

console.log(greeting.toLowerCase());
// Mengubah semua karakter dalam string greeting menjadi huruf kecil.

console.log(greeting.replace("Hello", "Hi"));
// Mengganti kata "Hello" dengan "Hi" dalam string greeting.

console.log(greeting.split(" "));
// Memecah string greeting menjadi array berdasarkan spasi.

console.log(greeting.length);
// Menampilkan panjang dari string greeting, yaitu jumlah karakternya.
